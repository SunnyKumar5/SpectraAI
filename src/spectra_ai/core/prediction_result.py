"""
Structure prediction result models for SpectraAI.

Holds ranked candidate structures generated by the AI engine
with confidence scores, explanations, and spectral match details.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Optional


@dataclass
class StructureCandidate:
    """
    A single candidate structure proposed by the prediction engine.

    Attributes:
        rank:           Position in ranked list (1 = best match)
        smiles:         SMILES string of the candidate
        name:           Suggested name (if known)
        formula:        Molecular formula
        confidence:     Match confidence score (0.0 – 1.0)
        explanation:    Why this candidate was proposed
        matching_peaks: List of peaks that support this candidate
        conflicting_peaks: List of peaks that conflict with this candidate
        scaffold_family: Detected scaffold family
    """

    rank: int = 0
    smiles: str = ""
    name: str = ""
    formula: str = ""
    confidence: float = 0.0
    explanation: str = ""
    matching_peaks: list[str] = field(default_factory=list)
    conflicting_peaks: list[str] = field(default_factory=list)
    scaffold_family: str = ""

    @property
    def confidence_percent(self) -> float:
        return round(self.confidence * 100, 1)

    @property
    def confidence_label(self) -> str:
        if self.confidence >= 0.9:
            return "Very High"
        elif self.confidence >= 0.75:
            return "High"
        elif self.confidence >= 0.6:
            return "Medium"
        elif self.confidence >= 0.4:
            return "Low"
        else:
            return "Very Low"

    @property
    def status_color(self) -> str:
        """Return a CSS-style color for the confidence level."""
        if self.confidence >= 0.8:
            return "#22c55e"   # green
        elif self.confidence >= 0.6:
            return "#f59e0b"   # amber
        else:
            return "#ef4444"   # red

    def to_dict(self) -> dict:
        return {
            "rank": self.rank,
            "smiles": self.smiles,
            "name": self.name,
            "formula": self.formula,
            "confidence": self.confidence,
            "explanation": self.explanation,
            "matching_peaks": self.matching_peaks,
            "conflicting_peaks": self.conflicting_peaks,
            "scaffold_family": self.scaffold_family,
        }

    @classmethod
    def from_dict(cls, data: dict) -> "StructureCandidate":
        return cls(**{k: v for k, v in data.items() if k in cls.__dataclass_fields__})


@dataclass
class PredictionResult:
    """
    Complete structure prediction result.

    Attributes:
        candidates:     Ranked list of candidate structures
        method:         Prediction method used (e.g. "scaffold-constrained", "open")
        scaffold_constraint: Scaffold family used as constraint (if any)
        reaction_context: Reaction info used to inform prediction (if any)
        reasoning:      AI's overall reasoning chain
        warnings:       Any warnings generated during prediction
    """

    candidates: list[StructureCandidate] = field(default_factory=list)
    method: str = "scaffold-constrained"
    scaffold_constraint: str = ""
    reaction_context: str = ""
    reasoning: str = ""
    warnings: list[str] = field(default_factory=list)

    @property
    def top_candidate(self) -> Optional[StructureCandidate]:
        """Return the highest-ranked candidate."""
        if not self.candidates:
            return None
        return self.candidates[0]

    @property
    def candidate_count(self) -> int:
        return len(self.candidates)

    @property
    def has_high_confidence_match(self) -> bool:
        """Check if any candidate has confidence >= 0.8."""
        return any(c.confidence >= 0.8 for c in self.candidates)

    def to_dict(self) -> dict:
        return {
            "candidates": [c.to_dict() for c in self.candidates],
            "method": self.method,
            "scaffold_constraint": self.scaffold_constraint,
            "reaction_context": self.reaction_context,
            "reasoning": self.reasoning,
            "warnings": self.warnings,
        }

    @classmethod
    def from_dict(cls, data: dict) -> "PredictionResult":
        candidates = [StructureCandidate.from_dict(c) for c in data.get("candidates", [])]
        return cls(
            candidates=candidates,
            method=data.get("method", ""),
            scaffold_constraint=data.get("scaffold_constraint", ""),
            reaction_context=data.get("reaction_context", ""),
            reasoning=data.get("reasoning", ""),
            warnings=data.get("warnings", []),
        )
